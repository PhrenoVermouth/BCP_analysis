import argparse
import json
import os
import sys
import html
from openai import OpenAI


MODEL_NAME = "gpt-5.2"

def parse_args():
    parser = argparse.ArgumentParser(description="Send debug report to ChatGPT for analysis.")
    parser.add_argument("report_json", help="Path to the JSON report generated by nf_debug_agent.py")

    parser.add_argument(
        "--api-key",
        required=False,
        help="OpenAI API Key (starts with sk-...). If not provided, looks for OPENAI_API_KEY env var."
    )

    parser.add_argument(
        "--base-url",
        required=False,
        default="https://api.openai.com/v1",
        help="Custom Base URL (useful if you use a proxy)."
    )

    parser.add_argument(
        "--html",
        default="analysis_result.html",
        help="Output path for the HTML analysis report (default: analysis_result.html)."
    )

    return parser.parse_args()

def load_report(path):
    try:
        with open(path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except Exception as e:
        print(f"âŒ Error reading report file: {e}")
        sys.exit(1)

def build_prompt(report_data):
    system_message = (
        "You are an expert Bioinformatics Engineer and Python/R Developer. "
        "Analyze the provided Nextflow pipeline failure report.\n\n"
        "Instructions:\n"
        "1. 'failures': Contains error logs, exit codes, and file sizes.\n"
        "2. 'script_library': Contains the source code of referenced scripts.\n"
        "3. CRITICAL: When a task references a script in 'related_scripts', you MUST check its content in 'script_library' to find the logical error.\n"
        "4. Output format: Provide a 'Root Cause' and 'Suggested Fix' for each failure. Use Markdown formatting (headers, bold, code blocks)."
    )
    user_message = json.dumps(report_data, indent=2, ensure_ascii=False)
    return system_message, user_message

