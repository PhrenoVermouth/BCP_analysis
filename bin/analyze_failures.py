def main():
    args = parse_args()

    api_key = args.api_key or os.getenv("OPENAI_API_KEY")
    if not api_key:
        print("âŒ Error: API Key is missing!")
        sys.exit(1)

    client = OpenAI(
        api_key=api_key,
        base_url=args.base_url
    )

    print(f"ğŸ“‚ Loading report: {args.report_json}...")
    report_data = load_report(args.report_json)

    failures = report_data.get("failures", [])
    if not failures:
        print("âœ… No failures found. Report is clean.")
        sys.exit(0)

    print(f"ğŸ¤– Analyzing {len(failures)} failures using {MODEL_NAME}...\n" + "="*50 + "\n")

    system_msg, user_msg = build_prompt(report_data)

    full_response = ""

    try:
        stream = client.chat.completions.create(
            model=MODEL_NAME,
            messages=[
                {"role": "system", "content": system_msg},
                {"role": "user", "content": f"Here is the failure report:\n\n{user_msg}"}
            ],
            stream=True,
            temperature=0.2
        )

        for chunk in stream:
            if chunk.choices[0].delta.content is not None:
                content = chunk.choices[0].delta.content
                print(content, end="", flush=True) # ä¾ç„¶æ‰“å°åˆ°å±å¹•
                full_response += content

        print("\n\n" + "="*50 + "\nAnalysis Complete.")

        # ä¿å­˜ HTML æŠ¥å‘Š
        save_html_report(full_response, args.html)

    except Exception as e:
        print(f"\nâŒ API Request Failed: {e}")

if __name__ == "__main__":
    main()
