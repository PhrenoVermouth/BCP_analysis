#!/usr/bin/env python3
import sys
import re
import json
import os

def parse_chromap(log_file):
    sample_name = os.path.basename(log_file).replace('.chromap.log', '')
    total = 0
    unique = 0
    multi = 0
    valid_barcodes = 0

    with open(log_file, 'r') as f:
        for line in f:
            if line.startswith("Number of reads:"):
                total = int(re.search(r'\d+', line.replace(',', '')).group())
            elif line.startswith("Number of uniquely mapped reads:"):
                unique = int(re.search(r'\d+', line.replace(',', '')).group())
            elif line.startswith("Number of reads have multi-mappings:"):
                multi = int(re.search(r'\d+', line.replace(',', '')).group())
            elif line.startswith("Number of output mappings (passed filters):"):
                valid_barcodes = int(re.search(r'\d+', line.replace(',', '')).group())

    unmapped = total - unique - multi

    data = {
        "id": "chromap_alignment",
        "section_name": "Chromap ATAC Alignment",
        "description": "Alignment statistics generated by Chromap for ATAC-seq fragments.",
        "plot_type": "bargraph",
        "pconfig": {
            "id": "chromap_alignment_plot",
            "title": "Chromap: Alignment Rates",
            "ylab": "Number of Reads",
            "cpswitch_counts_label": "Number of Reads",
            "colors": {
                "Mapped_Unique": "#3b82f6",
                "Mapped_Multi": "#10b981",
                "Unmapped": "#ef4444"
            }
        },
        "data": {
            sample_name: {
                "Mapped_Unique": unique,
                "Mapped_Multi": multi,
                "Unmapped": unmapped
            }
        }
    }
    
    with open(f"{sample_name}_chromap_mqc.json", 'w') as out:
        json.dump(data, out, indent=4)

if __name__ == "__main__":
    for arg in sys.argv[1:]:
        parse_chromap(arg)
